class User():
    
    def getUserID(self):
        return self.userID
    
    def setUserID(self, userID):
        self.userID = userID
        
    def getName(self):
        return self.name
    
    def setName(self, name):
        self.name = name
    
    def getPassword(self):
        return self.password
    
    def setPassword(self, password):
        self.password = password
        
    def getRole(self):
        return self.role
    
    def setRole(self, role):
        self.role = role
    
    
        